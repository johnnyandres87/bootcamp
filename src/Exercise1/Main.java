package Exercise1;

public class Main {

    public static void main(String[] args) {

        String valueString = "String";
        int valueInt = 123;
        long valueLong = 12L;
        double valueDouble = 2d;
        boolean valueBoolean = true;

        System.out.println("Types of values:\n" +
                "Type of value String: " + valueString + "\n" +
                "Type of value  int: " + valueInt + "\n" +
                "Type of value  Long: " + valueLong + "\n" +
                "Type of value  Double: " + valueDouble + "\n" +
                "Type of value  Boolean: " + valueBoolean);
    }
}
